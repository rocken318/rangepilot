import type { Position, Action } from '../types';

export interface SpotQuestion {
  id: string;
  /** ユーザーへの問題文 */
  question: string;
  /** 自分のポジション */
  myPosition: Position;
  /** 自分のハンド */
  hand: string;
  /** 場面の種類: open=自分がオープンする, vsOpen=誰かがオープン済み, vs3Bet=自分のオープンに3ベットが返ってきた */
  scenario: 'open' | 'vsOpen' | 'vs3Bet' | 'bbDefense' | 'sbVsBb';
  /** 相手のポジション（vsOpen/vs3Bet/bbDefense時） */
  openerPosition?: Position;
  /** 状況説明（UI表示用） */
  situationLabel: string;
  /** 選択肢として表示するアクション */
  choices: UserChoice[];
  /** レンジデータから引く際のキー情報 */
  lookupMode: 'open' | 'vsOpen' | 'vs3Bet' | 'bbDefense' | 'sbVsBb';
  lookupMyPos: Position;
  lookupOpenerPos?: Position;
  lookupSbBbScenario?: 'sbOpen' | 'bbDefVsSb';
  /** 解説（回答後に表示） */
  explanation: string;
  /** 安全寄りモードでの補足 */
  safeExplanation: string;
  /** 初心者向け注意点 */
  beginnerTip: string;
  /** 同じハンドが状況で変わることの解説 */
  contextNote: string;
  /** 3ベットサイズが大きい場合の注意（該当する場合のみ） */
  largeSizeNote?: string;
  /** タグ（フィルター用） */
  tags: string[];
}

export type UserChoice = 'raise' | 'call' | '3bet' | '4bet' | 'fold';

export const USER_CHOICE_LABELS: Record<UserChoice, string> = {
  raise: 'Raise',
  call: 'Call',
  '3bet': '3-Bet',
  '4bet': '4-Bet',
  fold: 'Fold',
};

/** ユーザーの選択をレンジデータのアクションに変換（正解判定用） */
export function choiceMatchesAction(choice: UserChoice, action: Action): boolean {
  switch (choice) {
    case 'raise':
      return action === 'raise';
    case 'call':
      return action === 'call';
    case '3bet':
      return action === '3betValue' || action === '3betBluff' || action === '3bet';
    case '4bet':
      return action === '4betValue' || action === '4betBluff';
    case 'fold':
      return action === 'fold';
  }
}

/** mixed は「正解でも不正解でもない」特殊扱い */
export function isMixedAction(action: Action): boolean {
  return action === 'mixed';
}

